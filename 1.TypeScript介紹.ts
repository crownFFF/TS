/*
  1-1.TypeScript介紹
      1-1.1 TypeScript是JS的超集(JS有的TS都有)
      1-1.2 TypeScript = Type + JavaScript(在JS基礎之上，為JS添加了類型支持)
      1-1.3 TypeScript是微軟開發的開源編程語言，可以在任何運行JS的地方運行

  1-2.TypeScript為什麼要為JS添加類型支持
      背景:JS的類型系同存在'先天缺陷'，JS代碼中絕大部分錯誤都是類型錯誤(UncaughtTypeError)
      
      從編成語言的動靜區分，TS屬於靜態類型，JS屬於動態類型
      靜態類型:編譯期做類型檢查
      動態類型:執行期做類型檢查
*/
